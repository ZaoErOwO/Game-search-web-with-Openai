package com.icss.entity;

public class GameType {
	private int tid;
	private String tname;
	private String tpd;
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getTpd() {
		return tpd;
	}
	public void setTpd(String tpd) {
		this.tpd = tpd;
	}
	
	
}
